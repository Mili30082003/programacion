/*  a. Escriba un programa que lea dos números enteros y los almacene en las variables a y b.
 Muestre en pantalla los valores leídos en a y b. */

 #include <stdio.h>

 int main () {

    // 1 - Declaro dos numeros enteros
       int n1 = 3;
       int n2 = 5;

      // 2 - Almaceno los numeros en las variables a y b
      int a = n1;
      int b = n2;

      // 3 - Muestro los valores por pantalla
      printf("Valor de n1: %d\n" , n1); 
      printf("Valor de n2: %d\n", n2);
      printf("Valor de a: %d\n", a);
      printf("Valor de b: %d\n", b);



    return 0;
 }